//to get list of keys / values of an object in array

let obj = {
  name: "Alice",
  age: 30,
  city: "Wonderland",
};

// Using Object.keys()
let keys = Object.keys(obj);

// Log the keys
console.log(keys);

let obj2 = {
  name: "Alice",
  age: 30,
  city: "Wonderland",
};

// Using Object.values()
let values = Object.values(obj2);

// Log the values
console.log(values);
